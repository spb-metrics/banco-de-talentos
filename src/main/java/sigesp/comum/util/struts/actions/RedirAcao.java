package sigesp.comum.util.struts.actions;

import org.apache.struts.action.Action;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 * Implementação de <strong>Action</strong> que não faz nada a não ser redirecionar para "sucesso".
 */
public final class RedirAcao extends Action
{
    /**
     * Process the specified HTTP request, and create the corresponding HTTP response (or forward to another web
     * component that will create it), with provision for handling exceptions thrown by the business logic.
     *
     * @param mapping The ActionMapping used to select this instance
     * @param form The optional ActionForm bean for this request (if any)
     * @param request The HTTP request we are processing
     * @param response The HTTP response we are creating
     *
     * @return 
     *
     * @exception Exception if the application business logic throws an exception
     */
    public ActionForward execute(ActionMapping mapping, ActionForm form, HttpServletRequest request,
        HttpServletResponse response)
            throws Exception
    {
        return (mapping.findForward("sucesso"));
    }
}
